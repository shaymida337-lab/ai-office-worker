/**
 * התאמת מילות מפתח בעברית עם תמיכה בתחיליות.
 *
 * \b של JS הוא ASCII-בלבד ולא עובד לעברית, ולכן ההתאמה נעשית עם lookaround
 * על טווח האותיות העברי. אבל lookbehind גורף שבר מילים עם תחיליות:
 * "הקבלה שלך על הזמנה ב-Google Play" נחסם כי ה' שלפני "קבלה" היא אות עברית.
 *
 * הפתרון: מתירים עד 3 אותיות תחילית (ה/ו/ב/ל/מ/ש/כ — "מהחשבונית",
 * "ולקבלה") לפני מילת המפתח, וה-lookbehind נבדק לפני התחיליות. מילה שבה
 * מילת המפתח מופיעה באמצע ("התקבלה" — ת' אינה תחילית) עדיין לא מותאמת.
 *
 * כיוון הבטיחות: false positive (התאמה מיותרת, למשל "שקבלה" כפועל) רק
 * מכניס מייל לבדיקה — false negative חוסם חשבונית אמיתית, וזה האסור.
 */

const HEBREW_RANGE = "֐-׿";
const HEBREW_PREFIXES = "[ובלמשכה]{0,3}";

export function hebrewKeywordPattern(words: string[]): RegExp {
  return new RegExp(`(?<![${HEBREW_RANGE}])${HEBREW_PREFIXES}(?:${words.join("|")})(?![${HEBREW_RANGE}])`);
}
